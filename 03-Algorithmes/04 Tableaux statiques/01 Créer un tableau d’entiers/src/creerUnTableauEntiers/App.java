package creerUnTableauEntiers;

import java.util.Arrays;

public class App {

	public static void main(String[] args) {

		 // Declare and initialize the 'nombres' array
        int[] nombres = new int[7];

        // Initialize each element of the array to 0
        for (int i = 0; i < nombres.length; i++) {
            nombres[i] = 0;
        }

        // Display the array (optional)
        System.out.println(Arrays.toString(nombres)); // Prints the array content

	}

}
